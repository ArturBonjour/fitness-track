const initRecommendations = async () => {
    try {
        await mongoose.connect(process.env.MONGODB_URI);
        console.log('Connected to MongoDB');

        // Очищаем существующие рекомендации
        await WorkoutRecommendation.deleteMany({});
        console.log('Cleared existing recommendations');

        // Добавляем новые рекомендации
        await WorkoutRecommendation.insertMany(recommendations);
        console.log('Added new recommendations');

        console.log('Initialization completed successfully');
    } catch (err) {
        console.error('Error during initialization:', err);
    } finally {
        await mongoose.disconnect();
    }
};

